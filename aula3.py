"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print("Luiz Otávio")

# Aspas duplas
print("Luiz Otávio")

# Escape
print("Luiz \"Otávio\"")

# r
print(r"Luiz \"Otávio")

# Aspas duplas dentro de aspas simples
print('Luiz "Otávio"')
